const TABLE: Record<ResultsLang, ResultsCopy> = {
  Arabic: ar,
  French: fr,
  English: en,
  Hindi: hi,
};

export function getResultsCopy(language: string | undefined, country?: string): {
  copy: ResultsCopy;
  dir: "rtl" | "ltr";
  lang: ResultsLang;
} {
  let resolved: ResultsLang = "English";
  if (language === "Arabic" || language === "French" || language === "English" || language === "Hindi") {
    resolved = language;
  } else if (country === "India") {
    resolved = "Hindi";
  } else if (country === "Morocco") {
    resolved = "Arabic";
  }
  return {
    copy: TABLE[resolved],
    dir: resolved === "Arabic" ? "rtl" : "ltr",
    lang: resolved,
  };
}
